package assignments_string;

public class StringContains {

	public static void main(String[] args) {
		String p="Hello Bengaluru how are you doing all";
		
		System.out.println(p.concat("are"));
		System.out.println(p);
		
		System.out.println(p.contains("are"));
		
		System.out.println(p.contains("all "));

	}

}
