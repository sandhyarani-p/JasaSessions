package javasessions;

public class FinalKeyword {

	public static void main(String[] args) {
		
		
		//final keyword is used to declare constant-values cannot be changed.
		final int days=7;
		System.out.println("there are 7 days"+ days);
	    
	    
	    int totalamount = 100* days;

	    System.out.println("the total"+ totalamount);
	}

}
