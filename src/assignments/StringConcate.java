public class StringConcate {

	public static void main(String[] args) {
		
		// concat "Hello Selenium" with a character 't'.

		
		String s= "Hello Selenium";
		
		char c= 't';
		
		System.out.println(s + c);
		
		System.out.println(s +  " 't' ");
		
		System.out.println(s +  "\" java \" ");
		
		
	}

}
